# todo 탈출문자

# \n : 줄바꿈
print("백문이 불여일견 \n백견이 불여일타")

# \" \" : 문장 내에서 따옴표
# 저는 "나도 코딩" 입니다.
print('저는 "나루코딩"입니다.')
print("저는 \"나모코딩\"입니다.")
print("저는 \'나밍코딩\'입니다.")

# \\ : 문장 내에서 \로 출력된다.
print("C:\\python\\study>")

# \r 커서를 맨 앞으로 이동
print("Red Apple\rPine")
# PineApple이 떠야하는데 Pine만 뜨고있음.
print("Red Apple\rDDD BANANA")
print("Test Str\r보라돌이 테스트")

# \b : 백스페이스 역할(한글자 삭제)
print("Redd\bApple")

# \t : 탭
# 4, 8 칸씩 이동
print("Red\t\t\tApple")